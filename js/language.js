document.addEventListener("DOMContentLoaded", function() {
  const languageToggle = document.getElementById('language-toggle');
  const languageIndicator = document.querySelector('.language-indicator');
  let currentLanguage = localStorage.getItem('language') || 'es';
  
  // Actualizar indicador seg√∫n idioma guardado
  updateLanguageIndicator();
  
  // Aplicar el idioma guardado al cargar
  applyLanguage();
  
  languageToggle.addEventListener('click', function() {
    currentLanguage = currentLanguage === 'es' ? 'en' : 'es';
    localStorage.setItem('language', currentLanguage);
    updateLanguageIndicator();
    applyLanguage();
  });
  
  function updateLanguageIndicator() {
    languageIndicator.textContent = currentLanguage.toUpperCase();
  }
  
  function applyLanguage() {
    // Aplicar traducciones para contenido HTML
    document.querySelectorAll('[data-i18n]').forEach(element => {
      const path = element.getAttribute('data-i18n').split('.');
      let translation = translations[currentLanguage];
      
      // Navegar por la estructura anidada
      for (const key of path) {
        if (translation && translation[key]) {
          translation = translation[key];
        } else {
          translation = null;
          break;
        }
      }
      
      if (translation) {
        element.innerHTML = translation;
      }
    });
    
    // Aplicar traducciones para placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
      const path = element.getAttribute('data-i18n-placeholder').split('.');
      let translation = translations[currentLanguage];
      
      // Navegar por la estructura anidada
      for (const key of path) {
        if (translation && translation[key]) {
          translation = translation[key];
        } else {
          translation = null;
          break;
        }
      }
      
      if (translation) {
        element.placeholder = translation;
      }
    });
  }
}); 